package jp.ac.dendai.im.cps.iwitutorial;

public class LogItem {
    private String name;
    private int timing;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTiming() {
        return timing;
    }

    public void setTiming(int timing) {
        this.timing = timing;
    }
}
